    public void listarTodosLosDatos() {

        DefaultTableModel modelo = (DefaultTableModel) tblDatos1.getModel();
        modelo.setRowCount(0);

        List<Estudiantes> lista = controladorEstudiante.lista();

        if (lista.isEmpty()) {

            System.out.println("la lista esta vacia");

        }

        for (Estudiantes estudiante : lista) {

            Object[] objeto = {estudiante.getCedula().trim(), estudiante.getNombre().trim(),
                estudiante.getApellido().trim(), estudiante.getDireccion().trim(), estudiante.getGenero().trim()};

            modelo.addRow(objeto);

        }

        tblDatos1.setModel(modelo);

    }

