    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed

        String cedula = txtCedula.getText();
        String nombre = txtNombre.getText();
        String apellido = txtApellido.getText();
        String direccion = txtDireccion.getText();
        String genero = txtGenero.getText();

        if (cedula.isEmpty() || nombre.isEmpty() || apellido.isEmpty() || direccion.isEmpty() || genero.isEmpty()) {

            JOptionPane.showMessageDialog(this, "Llene todos los campos porfavor");

        } else {

            Estudiantes estudiante = new Estudiantes(cedula, nombre, apellido, direccion, genero);

            boolean cent = controladorEstudiante.update(estudiante);

            if (cent == true) {

                listar(estudiante);

            }

        }

    }//GEN-LAST:event_btnActualizarActionPerformed

